//콘솔에 출력하는 일을 하는 자바 소스코드 작성
class UsePrintMethod{

          public static void main(String[] args){
               
                //System.out.print("안녕");
                //System.out.print("하세요");

                  System.out.println("--------상수출력------");
                  System.out.println(2023);//정수상수
                  System.out.println(2023.06);//실수상수
                  System.out.println(true);//불린상수
                  System.out.println('A');//문자상수
                  System.out.println("2023년 6월 7일");//문자열상수

                  System.out.println("--------연산결과출력------");
                  System.out.println(6+7);
                 System.out.println("6+7="+ (6+7));
              }

}
